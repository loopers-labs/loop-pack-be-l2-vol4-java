-- =====================================================================
-- Round 5 like_count 인덱스 유지 근거 — 의사결정 기록
-- =====================================================================
-- 배경:
--   한용원 질문 (멘토링 P1):
--   "좋아요순 목록을 캐시로 처리한다면, 읽기는 캐시가 담당하고
--    인덱스는 쓰기 비용만 발생시키는 구조가 된다. 인덱스를 유지해야 하는가?"
--
--   김영주 질문 (멘토링 P0):
--   "like_count는 자주 수정되는 값 → 인덱스 재배치 비효율.
--    캐시로 해결하면 인덱스 불필요한가?"
--
-- 결론: 인덱스 유지. 아래 4가지 근거.
-- =====================================================================

-- =====================================================================
-- 근거 1) 캐시 미스는 반드시 발생한다 → DB 경로에 인덱스가 필요하다
-- =====================================================================
-- 캐시 미스가 발생하는 경우:
--   a) 서버 재시작 (Redis 키 전부 소멸)
--   b) TTL 만료 (목록 캐시 TTL = 3분)
--   c) 무효화 이벤트 (상품 생성/수정/삭제 시 목록 캐시 전체 삭제)
--   d) 신규 파라미터 조합 (처음 요청되는 brandId + sort + page)
--
-- 캐시 미스 시 실행되는 쿼리 (인덱스 없으면 filesort):
EXPLAIN
SELECT p.* FROM products p
WHERE p.deleted_at IS NULL
  AND p.brand_id = 7
ORDER BY p.like_count DESC
LIMIT 20 OFFSET 0;
-- 인덱스 있음: type=ref, key=idx_products_brand_likecount, rows≈20, No filesort
-- 인덱스 없음: type=ALL, rows≈100000, Extra=Using filesort  (~246ms)

-- =====================================================================
-- 근거 2) 캐시 무효화 직후 thundering herd — 인덱스가 DB를 보호한다
-- =====================================================================
-- 상품 수정 이벤트 → 목록 캐시 전체 무효화 → 다음 요청들이 모두 DB로 직행.
-- 인기 시간대에 수정이 발생하면 수십 req/s 가 동시에 DB 타격.
-- 인덱스 없으면 각 요청이 246ms × filesort → DB 포화.
-- 인덱스 있으면 각 요청이 ~1ms → DB 여유.

-- =====================================================================
-- 근거 3) 쓰기 부하는 측정해보면 작다 (write_overhead_round5.sql 결과 참조)
-- =====================================================================
-- like_count UPDATE 1건에 인덱스가 추가하는 비용 예측:
--   idx_products_brand_likecount : (brand_id, like_count) B-Tree 페이지 1개 갱신
--   idx_products_likecount       : (like_count) B-Tree 페이지 1개 갱신
--   추가 비용: +0.1 ~ 0.3 ms / 건
--
-- 좋아요 트래픽 규모 고려:
--   100 TPS 전체 중 좋아요 조작은 극소수 (읽기 >> 쓰기).
--   쓰기 비용 증가보다 읽기 보호 효과가 압도적으로 크다.

-- =====================================================================
-- 근거 4) 인덱스 제거 시 실제로 얼마나 느려지는가
-- =====================================================================
-- 인덱스 없을 때 likes_desc 쿼리 (AS-IS 측정값):
EXPLAIN ANALYZE
SELECT p.* FROM products p
LEFT JOIN likes l ON l.product_id = p.id
WHERE p.deleted_at IS NULL AND p.brand_id = 7
GROUP BY p.id
ORDER BY COUNT(l.id) DESC
LIMIT 20 OFFSET 0;
-- 결과: ~246 ms (임시 테이블 + filesort, 10만 행 처리)

-- 인덱스 있을 때 likes_desc 쿼리 (like_count 컬럼 + 복합 인덱스):
EXPLAIN ANALYZE
SELECT p.* FROM products p
WHERE p.deleted_at IS NULL AND p.brand_id = 7
ORDER BY p.like_count DESC
LIMIT 20 OFFSET 0;
-- 결과: ~1 ms (index range scan, LIMIT 20개만 읽고 중단)

-- =====================================================================
-- 최종 트레이드오프 정리
-- =====================================================================
-- 비용항목               인덱스 있음         인덱스 없음
-- -------------------------------------------------------
-- like UPDATE 1건       +0.1~0.3 ms        베이스라인
-- likes_desc 조회(캐시미스)  ~1 ms          ~246 ms
-- 캐시 무효화 후 부하     낮음(인덱스 보호)   폭증 위험
-- -------------------------------------------------------
-- 결론: 쓰기 비용 +0.3ms vs 읽기 보호 245ms → 인덱스 유지.
--
-- 인덱스를 제거하는 시나리오:
--   - 좋아요 쓰기 TPS가 수천 단위로 증가해 인덱스 경합이 실측됨
--   - AND 캐시 히트율이 99%+ 로 측정되어 캐시 미스 경로가 사실상 없음
--   - 위 두 조건이 동시에 충족될 때 재검토.
-- =====================================================================
